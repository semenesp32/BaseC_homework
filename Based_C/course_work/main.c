/*

Программа является консольным приложением и рекомендуется запускать её из командной строки.
При запуске без указания ключей, программа кратко выведет информацию о своем назначении и
предложении ввести ключ «-h» для получения инструкций.

Программа допускает применение следующих ключей:
●	«-h» - получение информации о возможных ключах запуска с кратким описанием их назначения;
●	«-f file_name» - указание файла для обработки, где file_name – имя файла. Если в этом режиме не добавлена опция «-m», то выведется полная статистика по всему файлу;
●	«-m xx» - указание месяца для получения статистики по конкретному месяцу, где xx - месяц;

Примеры написания командной строки для запуска:
.\prog -h
.\prog -f ../info/temperature_big.csv
.\prog -f ../info/temperature_big.csv -m 5

Программа состоит из следующих файлов:
makefile – файл инструкция для утилиты сборки mingw32-make;
prog.exe – собранный исполняемый файл программы;
main.c – основной файл программы с точкой входа, функцией main. Содержит только логику запуска функций и обработку ключей, поступающих от командной строки;
temp_function.c – файл описания функций программы;
Папка src:
temp_function.h – файл прототипов функций программы.
Папка info:
*.csv – файлы, в которых хранятся данные со статистикой

*/

#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>
#include <string.h>
#include "temp_functions.h"

int main(int argc, char *argv[])
{
    sensor *info = NULL;
    int number = 0;

    while (1)
    {
        ArgsCommandLine(argc, argv, &info, &number);

        char input[256];
        if (!fgets(input, sizeof(input), stdin))
        {
            break;
        }

        argc = 0;
        char *token = strtok(input, " \n");
        while (token && argc < 256)
        {
            argv[argc++] = token;
            token = strtok(NULL, " \n");
        }
    }

    free(info);
    return 0;
}